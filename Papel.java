public class Papel {
  private String nome;
  private String ator;
  private String filme;


  public String getNome()
  {
    return nome;
  }
  public void setNome(String n)
  {
    nome = n;
  }


  public String getAtor()
  {
    return ator;
  }
  public void setAtor(String a)
  {
    ator = a; 
  }


  public String getFilme()
  {
    return filme;
  }
  public void setFilme(String f)
  {
    filme = f;
  }



}