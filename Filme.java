public class Filme{
  private String titulo;
  private int duracao;
  private Genero genero;

  
  public String getTitulo()
  {
    return titulo;
  }
  public void setTitulo(String t)
  {
    titulo = t; 
  }

  
  public int getDuracao()
  {
    return duracao; 
  }
  public void setDuracao(int du)
  {
    duracao = du;
  }


  public Genero getGenero()
  {
    return genero; 
  }
  public void setGenero(Genero g)
  {
    genero = g; 
  }

  













  
}